from linked_lists.singly_linked_lists import Node


class Stack:
    def __init__(self, value):
        new_node = Node(value)
        self.top = new_node
        self.height = 1

    def print_stack(self):
        temp = self.top
        print("------- Printing stack starts -------")
        while temp is not None:
            print(temp.value)
            temp = temp.next
        print("------- Printing stack ends -------")

    def push(self, value):
        new_node = Node(value)
        if self.height == 0:
            self.top = new_node
        else:
            new_node.next = self.top
            self.top = new_node
        self.height += 1
        return new_node

    def pop(self):
        if self.height == 0:
            return None
        temp = self.top
        self.top = self.top.next
        temp.next = None
        self.height -= 1
        return temp


stack = Stack(7)
stack.print_stack()
